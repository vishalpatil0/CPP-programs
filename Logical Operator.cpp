#include<iostream>
using namespace std;
int main()
{
    //comparision
    cout<<"10 == 10 --> "<<(10==10)<<endl;

    //logical not
    cout<<"10 != 10 --> "<<(10!=10)<<endl;

    //logical and
    cout<<"10==10 && 11!=11 --> "<<(10==10 && 11!=11)<<endl;
    
    //logical OR
    cout<<"10==10 || 11!=11 --> "<<(10==10 || 11!=11)<<endl;

    return 0;
}