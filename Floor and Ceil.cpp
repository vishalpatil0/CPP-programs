#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    cout << "Floor is : " << floor(2.3) << endl;    //2
    cout << "Floor is : " << floor(-2.3) << endl;   //-3

    cout << " Ceil is : " << ceil(2.3) << endl;     //3
    cout << " Ceil is : " << ceil(-2.3) << endl;    //-2
    
    return 0;
}